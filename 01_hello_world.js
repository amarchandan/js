// Topic     : console.log()
// Level     : Beginner

/*
--------------------------------------------
What is console.log()?
--------------------------------------------
console.log() is a built-in JavaScript function used
to print data to the console. It is commonly used for:

1. Displaying output.
2. Debugging code.
3. Checking variable values.
4. Testing functions.
5. Understanding program flow.

--------------------------------------------
Syntax
--------------------------------------------
console.log(value);

value -> The data you want to print.
It can be:
- String
- Number
- Boolean
- Variable
- Object
- Array
- Function result
- Multiple values

--------------------------------------------
Example 1
--------------------------------------------
*/

console.log("Meow");

// Output:
// Meow

/*
--------------------------------------------
Example 2
--------------------------------------------
*/

console.log(100);

// Output:
// 100

/*
--------------------------------------------
Example 3
--------------------------------------------
*/

let name = "Amar";
console.log(name);

// Output:
// Amar

/*
--------------------------------------------
Example 4
--------------------------------------------
*/

console.log("Age:", 20);

// Output:
// Age: 20

/*
--------------------------------------------
Important Notes
--------------------------------------------

• console.log() only displays data.
• It does NOT save data.
• It does NOT return a value.
• Mostly used during development.
• The output appears in the browser's Developer Console
  or in the terminal when using Node.js.

--------------------------------------------
Real-World Uses
--------------------------------------------

✔ Debugging applications
✔ Printing API responses
✔ Checking variable values
✔ Tracking program execution
✔ Finding errors
✔ Learning JavaScript

--------------------------------------------
Related Methods
--------------------------------------------

console.error()   -> Prints an error message.
console.warn()    -> Prints a warning.
console.table()   -> Displays data as a table.
console.info()    -> Prints informational messages.
console.clear()   -> Clears the console.

--------------------------------------------
Summary
--------------------------------------------

Function : console.log()

Purpose  : Print data to the console.

Returns  : undefined

Parameters:
- Any value or multiple values

Used In:
- Browser
- Node.js
- Debugging
- Development

--------------------------------------------
Output
--------------------------------------------

Meow

*/